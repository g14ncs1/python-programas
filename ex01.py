#O seguinte programa calcula a área de um retângulo
print("Este programa calcula a área de um retângulo, dadas sua altura e base")
altura = float(input("Entre com a altura de um retângulo: "))
base = float(input("Entre com a base de um retângulo: "))
#As variáveis foram declaradas float para que não haja erro ou perda de precisão na multiplicação
print("A área do retângulo dado é", altura * base)
